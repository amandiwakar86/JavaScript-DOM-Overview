// -------------> What is the DOM <-------------//

/* 
The DOM is w3c ( World Wide Web Consortium ) standard
The DOM defines a standard for accessing documents:
*/

/*
The w3c Document Object Model (DOM) is a platform and language-neutral interface that allow prorams and scripts to dynamically access and update the contant, structure, and style of a document.
*/

// The W3C DOM standard is separated into 3 different parts:

/*
1. Core DOM - standard model for all document types
2. XML DOM - srandard model for XML documents
3. HTML DOM - standard model for HTML documents
*/

// What is the HTML DOM

/*
The HTML DOM is a standard object model and programming interface for HTML. It define:
1. The HTML elements as objects
2. The properties of all HTML elements
3. The methods to access all HTML elements
4. The events for all the HTML elements
*/

// In other words: The HTML DOM is a standard for how to get, change, add or delete HTML elements.